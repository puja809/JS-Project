// Primitive Datatypes:Memory allocation is stack
// String
 let name="Puja";
 console.log("My string is "+name);
 console.log("My type of variable is "+ (typeof name));
 
 //  Number
 let num=34;
 console.log("My number is "+num);
 console.log("My type of variable is "+ (typeof num));
 
 //  Boolean
 let bool=true;
 console.log("My boolean is "+bool);
 console.log("My type of variable is "+ (typeof bool));
 
 //  Null
 let empty=null;
 console.log("My empty is "+empty);
 console.log("My type of variable is "+ (typeof empty));
 
 //  undefined
 let und;
 console.log("My value is "+und);
 console.log("My type of variable is "+ (typeof und));
 
